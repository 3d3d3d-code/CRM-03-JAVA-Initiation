package com.crm.api;

public class Utils {

	public static float perimetre(float l, float L) {
		float p = (l + L) * 2;
		return p;

	}

	public static int somme(int a, int b) {
		int s = a + b;
		return s;

	}

	public static void afficherMessage(String texte) {

		System.out.println(texte);

	}

}
